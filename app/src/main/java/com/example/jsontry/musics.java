package com.example.jsontry;

public class musics {

    String name;
    int path;

    public musics(String name, int path) {
        this.name = name;
        this.path = path;
    }

    public int getPath() {
        return path;
    }

    public void setPath(int path) {
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}

